    private void bt_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluirActionPerformed
        int opcao_escolhida = JOptionPane.showConfirmDialog(null, "Excluir " + tf_login.getText() + "?", "Exclusão ", JOptionPane.YES_NO_OPTION);
        if (opcao_escolhida == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(tf_codigo.getText());
            CadUsuarioDAO dao = new CadUsuarioDAO();
            dao.remover(id);

            estadoBotoes("inicial");
        }
        limpaCampos();
        atualizaTela();
    }//GEN-LAST:event_bt_excluirActionPerformed

